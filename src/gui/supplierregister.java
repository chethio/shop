    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String name = jTextField1.getText();
        String contact = jTextField2.getText();
        String email = jTextField3.getText().toLowerCase();
        String bid = jLabel7.getText();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("0[0-9]{9}").matcher(contact).matches()) {
            JOptionPane.showMessageDialog(this, "Please enter valid mobile no.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter email", "Warning", JOptionPane.WARNING_MESSAGE);
        } //        else if(!Pattern.compile("^[A-Z0-9._%+-]+@[A-Z0-9.-]+\\\\.[A-Z]{2,6}$").matcher(email).matches()){
        //            JOptionPane.showMessageDialog(this, "Please enter valid email","Warning",JOptionPane.WARNING_MESSAGE);
        //        }
        else if (bid.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please select a branch", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {
                ResultSet rs = mysql.search("SELECT * FROM supplier WHERE email='" + email + "' OR contact='" + contact + "';");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "Supplier already exist", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    mysql.iud("INSERT INTO `supplier`(`name`,`contact`,`email`,`company_branch_id`) VALUES('" + name + "','" + contact + "','" + email + "','" + bid + "') ");
                    loadsupplier();
                    JOptionPane.showMessageDialog(this, "Supplier account created", "success", JOptionPane.INFORMATION_MESSAGE);
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jLabel7.setText("None");
                    jLabel8.setText("None");
                    jLabel9.setText("None");
                    jLabel11.setText("None");
                    jLabel13.setText("None");
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        if (evt.getClickCount() == 2) {
            int r = jTable1.getSelectedRow();

            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Please sselect a company", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                String sid = jTable1.getValueAt(r, 0).toString();
                String sname = jTable1.getValueAt(r, 1).toString();
                String contact = jTable1.getValueAt(r, 2).toString();
                String branch = jTable1.getValueAt(r, 4).toString();

                gr.jLabel2.setText(sid);
                gr.jLabel4.setText(sname);
                gr.jLabel6.setText(contact);
                gr.jLabel8.setText(branch);
                this.dispose();
            }
        }

    }//GEN-LAST:event_jTable1MouseClicked

